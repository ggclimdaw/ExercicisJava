public class App {
    public static void main(String[] args) throws Exception {

        int tipoInt = 12;
        short tipoShort = 32000;
        byte tipoByte = 11;
        long tipoLong = 1111L;
        boolean tipoBoolean = false;
        float tipoFloat = 3.14F;
        double tipoDouble = 3.14;
        char letra = 'a';
        char codigo = 97 + 1;

        System.out.println("El valor del int es " + tipoInt);
        System.out.println("El valor del short es " + tipoShort);
        System.out.println("El valor del byte es " + tipoByte);
        System.out.println("El valor del long es " + tipoLong);
        System.out.println("El valor del boolean es " + tipoBoolean);
        System.out.println("El valor del char es " + letra + "   " + codigo);
        System.out.println("El valor del float es " + tipoFloat);
        System.out.println("El valor del double es " + tipoDouble);

    }
}
