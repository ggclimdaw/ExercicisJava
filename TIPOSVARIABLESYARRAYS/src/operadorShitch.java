public class operadorShitch {
    public static void main(String[] args) throws Exception {
        int a = 2;

        switch (a) {
        case 2:
            System.out.println("El valor es 2");
            break;
        case 3:
            System.out.println("El valor es 3");
            break;
        default:
            System.out.println("El valor no es ni 2 ni 3");
        }

    }

}
